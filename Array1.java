public class Array1 {
    public static void main(String[] args) {
        String[] namaKota = {"Malang", "Surabaya", "Bandung", "Jakarta", "Semarang", "Yogyakarta", "Bali", "Medan", "Padang", "Makassar"};
        
        System.out.println("Daftar Nama kota :");
        for (int i = 0; i < namaKota.length; i++) {
            System.out.println(namaKota[i]);
        }
    }
}